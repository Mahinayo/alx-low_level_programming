/**
 * op_add - add two numbers
 * @a: int a
 * @b: int b
 * Return: result
**/
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - substracts two numbers
 * @a: int a
 * @b: int b
 * Return: result
**/
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - multiply two numbers
 * @a: int a
 * @b: int b
 * Return: result
**/
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - divide two numbers
 * @a: int a
 * @b: int b
 * Return: result
**/
int op_sub(int a, int b)
{
return (a / b);
}
/**
 * op_mod - modules two numbers
 * @a: int a
 * @b: int b
 * Return: result
**/
int op_mod(int a, int b)
{
return (a % b);
}
